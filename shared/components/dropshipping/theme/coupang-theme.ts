// Utility functions for theme usage
export const getThemeColor = (color: string, shade: number = 500) => {
  const [colorName, customShade] = color.includes('-') 
    ? color.split('-') 
    : [color, shade.toString()];
  
  const colorMap = CoupangTheme.colors[colorName as keyof typeof CoupangTheme.colors];
  if (!colorMap) return color;
  
  return colorMap[customShade as keyof typeof colorMap] || color;
};

export const getThemeShadow = (size: keyof typeof CoupangTheme.shadows) => {
  return CoupangTheme.shadows[size];
};

export const getThemeSpacing = (size: keyof typeof CoupangTheme.spacing) => {
  return CoupangTheme.spacing[size];
};